        <p>In order to remove a property from the application, go to the <a href="/landlord/update-room-availability">Update a room's availability</a> page and ensure all rooms are switched to unavailable. This will hide the property from the application's listings from a student's perspective.</p>
        <p>By hiding a property in this manner, it means that re-adding the property to the application the following year is quick and easy.</p>
        <p>If you would like to completely remove your property from Student Rooma, please contact <a href="mailto:support@studentrooma.co.uk">support@studentrooma.co.uk</a>.</p>
